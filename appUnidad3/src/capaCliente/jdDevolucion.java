    private void btnDevolucionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDevolucionActionPerformed
        // TODO add your handling code here:
        Float  monto = (Float) tblVenta.getValueAt(tblVenta.getSelectedRow(),4);
        Float txtMon = Float.parseFloat(txtMonto.getText());              
        Integer  cod = (Integer) tblVenta.getValueAt(tblVenta.getSelectedRow(),0);
        String motivo = txtMotivo.getText();
        if (txtMon > monto){            
            JOptionPane.showMessageDialog(this,"No se puede hacer la devolución, ya que el monto es mayor a la venta");
            txtMonto.setFocusable(true);
        } else {            
            if (motivo == "") {
                JOptionPane.showMessageDialog(this,"No se puede hacer la devolución, ya que no existe motivo");
                txtMotivo.setFocusable(true);
            } else {
                try {
                    objVenta.GestionarDevolucion(cod, motivo, txtMon, tblDetalle);
                    this.dispose();
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(this,"Error al hacer la devolución");
                    Logger.getLogger(jdDevolucion.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_btnDevolucionActionPerformed

