    public void listarFiltro(){
        
        ResultSet rs = null;
        DefaultTableModel modeloC = new DefaultTableModel( );
        modeloC.addColumn("C√≥digo");
        modeloC.addColumn("Producto");
        modeloC.addColumn("Descripci√≥n");
        modeloC.addColumn("Precio");
        modeloC.addColumn("Cantidad");
        modeloC.addColumn("Vigencia");
        tbl.setModel(modeloC);
        
        String opcion = cboOpcion.getSelectedItem().toString();
        
        try {
            if(opcion.equals("vigencia")){
            rs=obj.Filtro(opcion,0,0,0,0,this.disponibilidad.isSelected());
                while(rs.next()){
                modeloC.addRow(new Object[]{rs.getInt("codigo"),rs.getString("producto"),rs.getString("descripci√≥n"),rs.getFloat("precio"),rs.getInt("cantidad"),rs.getBoolean("disponibilidad")});                
                }
            }else{
                if(opcion.equals("stock")){
                    rs=obj.Filtro(opcion,0,0,Integer.parseInt(v1.getText()),Integer.parseInt(v2.getText()),false);
                    while(rs.next()){
                    modeloC.addRow(new Object[]{rs.getInt("codigo"),rs.getString("producto"),rs.getString("descripci√≥n"),rs.getFloat("precio"),rs.getInt("cantidad"),rs.getBoolean("disponibilidad")});                
                    }
                }else{
                     rs=obj.Filtro(opcion,Float.parseFloat(v1.getText()),Float.parseFloat(v2.getText()),0,0,false);
                     while(rs.next()){
                    modeloC.addRow(new Object[]{rs.getInt("codigo"),rs.getString("producto"),rs.getString("descripci√≥n"),rs.getFloat("precio"),rs.getInt("cantidad"),rs.getBoolean("disponibilidad")});                
                    }
                }
            }
            
            /*while(rs.next()){
                modeloC.addRow(new Object[]{rs.getInt("codigo"),rs.getString("producto"),rs.getString("descripci√≥n"),rs.getFloat("precio"),rs.getInt("cantidad"),rs.getBoolean("disponibilidad")});                
           }
            */
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Hubo alg√∫n error al enviar los datos");
        }

    }
