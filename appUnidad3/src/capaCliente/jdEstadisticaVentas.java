    private void listar(){
        ResultSet rsPro = null;
        DefaultTableModel modelo = new DefaultTableModel( );
        modelo.addColumn("NÃºmero");
        modelo.addColumn("Fecha");
        modelo.addColumn("Cliente");
        modelo.addColumn("Total");
        modelo.addColumn("Producto");
        modelo.addColumn("Cantidad");
        modelo.addColumn("Descuento");
        tbl.setModel(modelo);
        try {
            rsPro = objventa.BUSQUEDA(Integer.parseInt(txtventa.getText()));
            while(rsPro.next()){
              modelo.addRow(new Object[]{rsPro.getInt("numero"),rsPro.getString("fecha"),rsPro.getString("cliente"),rsPro.getFloat("total"),rsPro.getString("producto"),rsPro.getInt("cantidad"),rsPro.getInt("descuento")});
           }
        } catch (Exception e) {
            Logger.getLogger(jdEstadisticaVentas.class.getName()).log(Level.SEVERE, null, e);
        }
    }
    

