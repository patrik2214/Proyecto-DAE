    public void listarVentas(){
        ResultSet rsCliente = null;
        DefaultTableModel modeloC = new DefaultTableModel( );
        modeloC.addColumn("N° Venta");
        modeloC.addColumn("Fecha");
        modeloC.addColumn("I.G.V");
        modeloC.addColumn("Sub-total");
        modeloC.addColumn("Total");
        modeloC.addColumn("Estado Pago");
        tblVenta.setModel(modeloC);
        try {
            rsCliente=objVenta.listarVenta(txtDni.getText());
            while(rsCliente.next()){
                modeloC.addRow(new Object[]{rsCliente.getInt("numventa"),rsCliente.getString("fecha"),rsCliente.getFloat("igv"),rsCliente.getFloat("subtotal"),rsCliente.getFloat("total"),rsCliente.getBoolean("estadopago")});                
           }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,"Error al consultar Ventas");
        }
    }
    
    public void listarDetalleVentas(Integer cod){
        ResultSet rsCliente = null;
        DefaultTableModel modeloC = new DefaultTableModel( );
        modeloC.addColumn("N° Venta");
        modeloC.addColumn("Producto");
        modeloC.addColumn("Cantidad");
        modeloC.addColumn("Precio Venta");
        modeloC.addColumn("Descuento");
        modeloC.addColumn("Sub-total");
        tblDetalle.setModel(modeloC);
        try {
            //Corregir función
            rsCliente=objVenta.listarDetalle(cod);
            while(rsCliente.next()){
                modeloC.addRow(new Object[]{rsCliente.getInt("numventa"),rsCliente.getInt("codproducto"),rsCliente.getInt("cantidad"),rsCliente.getFloat("precioventa"),rsCliente.getFloat("descuento"),rsCliente.getFloat("subtotal")});                
           }
        
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,"Error al consultar Ventas");
        }
    }
    
